import assert from 'node:assert/strict';
import fs from 'node:fs';
import vm from 'node:vm';

const source = fs.readFileSync('src/research/project-workspace.js', 'utf8');
const index = fs.readFileSync('index.html', 'utf8');
const engine = fs.readFileSync('src/research-engine.js', 'utf8');
new vm.Script(source, { filename: 'src/research/project-workspace.js' });
const memory = new Map();
const context = { console, window: {}, globalThis: {}, Date, Math, localStorage: { getItem: (k) => memory.has(k) ? memory.get(k) : null, setItem: (k, v) => memory.set(k, String(v)), removeItem: (k) => memory.delete(k) } };
context.window = context;
context.globalThis = context;
vm.createContext(context);
vm.runInContext(source, context, { filename: 'src/research/project-workspace.js' });
const workspace = context.Jarbou3iResearchModules.projectWorkspace;
assert.equal(workspace.VERSION, '0.26.0-beta');
assert.equal(workspace.MAX_PROJECTS, 25);
assert.ok(index.includes('id="projectWorkspacePanel"'), 'project workspace panel missing');
assert.ok(index.includes('id="saveProjectBtn"'), 'save project button missing');
assert.ok(index.includes('id="importProjectInput"'), 'project import input missing');
assert.ok(index.includes('src="src/research/project-workspace.js" defer'), 'project workspace script missing');
assert.ok(engine.includes('WORKSPACE_STORAGE_KEY'), 'engine must define workspace storage key');
assert.ok(engine.includes('projectWorkspace.metadata'), 'research packet must export workspace metadata');
assert.ok(engine.includes('saveCurrentProject'), 'engine must wire project save');
assert.ok(engine.includes('loadProject'), 'engine must support loading saved projects');
assert.ok(engine.includes('projectWorkspace.importBundle'), 'engine must support project import bundle');
let ws = workspace.emptyWorkspace();
assert.equal(ws.storage_mode, 'local_only');
assert.equal(ws.projects.length, 0);
const packet = { workflow_version: '0.26.0-beta', privacy_export: { key_exported: false, raw_token_exported: false } };
let saved = workspace.upsert(ws, packet, 'First project');
ws = saved.workspace;
assert.equal(ws.projects.length, 1);
assert.equal(ws.active_project_id, saved.project.project_id);
assert.equal(saved.project.name, 'First project');
const meta = workspace.metadata({ active_project_id: saved.project.project_id, active_project_name: saved.project.name }, ws);
assert.equal(meta.workspace_version, '0.26.0-beta');
assert.equal(meta.storage_mode, 'local_only');
assert.equal(meta.saved_project_count, 1);
assert.equal(meta.quota.project_count, 1);
const duplicated = workspace.duplicate(ws, saved.project.project_id);
assert.equal(duplicated.workspace.projects.length, 2);
assert.match(duplicated.project.name, /copy$/);
const bundle = workspace.exportBundle(duplicated.workspace, duplicated.project.project_id);
assert.equal(bundle.project_export_version, '0.26.0-beta');
assert.equal(bundle.storage_mode, 'local_only');
const imported = workspace.importBundle(ws, bundle);
assert.equal(imported.workspace.projects.length, 2);
assert.equal(imported.project.workflow_version, '0.26.0-beta');
const removed = workspace.remove(imported.workspace, imported.project.project_id);
assert.equal(removed.deleted, true);
const html = workspace.html(removed.workspace);
assert.ok(html.includes('local-only'));
assert.ok(html.includes('Project'));
console.log('Project workspace checks passed.');
process.exit(0);
